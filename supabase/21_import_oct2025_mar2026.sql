--   6. Encargada del turno:
--        si LAURA aparece como colab ese día → LAU
--        si no                              → Wal
--   7. Hora turno: 09:00 – 17:00. Estado: cerrado.
--   8. Arqueos: cuadrados (reportado = esperado, diferencia = 0).
--   9. Propinas: 0 (no se llevó registro).
