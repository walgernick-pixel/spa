select new.nombre, new.alias, true, '[IMPORT_2025_2026]'
from (values
  ('Sol',    'Sol'),
  ('Martha', 'Martha'),
  ('Arse',   'Arse'),
  ('Marlon', 'Marlon'),
  ('Nicol',  'Nicol'),
  ('Apoyo',  'Apoyo'),
  ('Caja',   'Caja')
) as new(nombre, alias)
