  ('FISH SPA', '7cd9dc5f-5e18-4f81-a965-9b9d202f1b97'::uuid)
) as t(tipo, servicio_id);

-- Canal según rango de %; FISH SPA y % null → caja
-- Función inline: si tipo='FISH SPA' o pct null o pct=0 → caja
--                 pct<45 → rol; pct<56 → modulo; else hotel
-- Comisión final del canal:
--   caja   = 0
--   rol    = 40
--   modulo = 50
--   hotel  = 60

-- ─────────────────────────────────────────────────────────────
-- 5) Validación de colaboradoras: si alguna del Excel NO existe
--    en _map_colab (después de crear las faltantes), abortar.
-- ─────────────────────────────────────────────────────────────
do $$
declare v_missing text;
begin
  select string_agg(distinct i.colab, ', ')
    into v_missing
    from _imp i
    left join _map_colab m on m.excel_name = i.colab
   where m.excel_name is null
     and i.colab not in ('JAVIER');  -- JAVIER va a gastos, no requiere colab
  if v_missing is not null then
    raise exception 'Colaboradoras no encontradas: %', v_missing;
  end if;
end $$;

-- ─────────────────────────────────────────────────────────────
-- 6) Crear turnos (uno por fecha)
--    encargada = LAU si Laura aparece ese día como colab,
--    si no, Wal.
-- ─────────────────────────────────────────────────────────────
with dias as (
  select distinct fecha
    from _imp
   where venta is not null and venta > 0
), encargada as (
  select
    d.fecha,
    case when exists (
      select 1 from _imp i
       where i.fecha = d.fecha
         and i.colab = 'LAURA'
         and i.venta is not null and i.venta > 0
    )
    then '4064d54a-089b-444f-86d8-96bf3c72766e'::uuid  -- LAU
    else '85133c85-7c94-4e9d-a33f-5dc9a7de900b'::uuid  -- Wal
    end as encargada_id
  from dias d
)
insert into turnos (fecha, hora_inicio, hora_fin, encargada_id, estado, notas, cerrado)
select
  e.fecha,
  '09:00'::time,
  '17:00'::time,
  e.encargada_id,
  'cerrado',
  '[IMPORT_2025_2026]',
  e.fecha + time '17:00'
from encargada e;

-- ─────────────────────────────────────────────────────────────
-- 7) turno_colaboradoras (marcar TODAS como pagadas hoy)
--    Esto permite que el arqueo descuente las comisiones.
-- ─────────────────────────────────────────────────────────────
insert into turno_colaboradoras (turno_id, colaboradora_id, comision_pagada_at)
select distinct
  t.id,
  m.colaboradora_id,
  t.fecha + time '17:00'
from turnos t
join _imp i on i.fecha = t.fecha and i.venta is not null and i.venta > 0
join _map_colab m on m.excel_name = i.colab
where t.notas like '%[IMPORT_2025_2026]%'
on conflict do nothing;

-- ─────────────────────────────────────────────────────────────
-- 8) Insertar ventas (filas con venta > 0)
--    Precio en moneda nativa (USD/CAD divididos entre tc).
--    Comisión calculada con el % del canal.
-- ─────────────────────────────────────────────────────────────
with ventas_norm as (
  select
    i.*,
    -- Canal según rango de pct y tipo
    case
      when i.tipo = 'FISH SPA' then 'caja'
      when i.pct is null or i.pct = 0 then 'caja'
      when i.pct < 45 then 'rol'
      when i.pct < 56 then 'modulo'
      else 'hotel'
    end as canal_id,
    case
      when i.tipo = 'FISH SPA' then 0
      when i.pct is null or i.pct = 0 then 0
      when i.pct < 45 then 40
      when i.pct < 56 then 50
      else 60
    end as canal_pct,
    mc.cuenta_id,
    mc.moneda,
    mc.tc,
    -- Precio en moneda nativa: USD/CAD se divide entre tc
    case when mc.moneda in ('USD','CAD') then i.venta / mc.tc else i.venta end as precio_nat,
    case when mc.moneda in ('USD','CAD') then coalesce(i.descuento, 0) / mc.tc else coalesce(i.descuento, 0) end as descuento_nat,
    ms.servicio_id,
    mco.colaboradora_id
  from _imp i
  join _map_cuenta   mc  on mc.fpago = i.fpago
  join _map_servicio ms  on ms.tipo = i.tipo
  join _map_colab    mco on mco.excel_name = i.colab
  where i.venta is not null and i.venta > 0
)
insert into ventas (
  turno_id, fecha, servicio_id, colaboradora_id, canal_id, cuenta_id,
  precio, descuento, comision_pct, comision_monto, propina,
  moneda, tc_momento, creado_por, creado
)
select
  t.id,
  v.fecha,
  v.servicio_id,
  v.colaboradora_id,
  v.canal_id,
  v.cuenta_id,
  v.precio_nat,
  v.descuento_nat,
  v.canal_pct,
  v.precio_nat * v.canal_pct / 100,
  0,
  v.moneda,
  v.tc,
  '85133c85-7c94-4e9d-a33f-5dc9a7de900b'::uuid,
  v.fecha + time '12:00'
from ventas_norm v
join turnos t on t.fecha = v.fecha and t.notas like '%[IMPORT_2025_2026]%';

-- ─────────────────────────────────────────────────────────────
-- 9) venta_pagos (1 fila por venta, tipo='servicio')
-- ─────────────────────────────────────────────────────────────
insert into venta_pagos (venta_id, cuenta_id, tipo, monto, descuento, orden)
select
  v.id,
  v.cuenta_id,
  'servicio',
  v.precio,
  coalesce(v.descuento, 0),
  0
from ventas v
join turnos t on t.id = v.turno_id
where t.notas like '%[IMPORT_2025_2026]%';

-- ─────────────────────────────────────────────────────────────
-- 10) Gastos consolidados — comisiones extras (filas sin venta)
--     Una fila por (fecha, colab, fpago) con la suma de comisiones.
-- ─────────────────────────────────────────────────────────────
with extras as (
  select
    i.fecha,
    i.colab,
    mc.cuenta_id,
    mc.moneda,
    mc.tc,
    sum(coalesce(i.comision, 0)) as monto_excel
  from _imp i
  join _map_cuenta mc on mc.fpago = i.fpago
  where (i.venta is null or i.venta = 0)
    and i.comision is not null
    and i.comision > 0
  group by i.fecha, i.colab, mc.cuenta_id, mc.moneda, mc.tc
)
insert into gastos (
  fecha, concepto_id, proveedor, cuenta_id,
  monto, moneda, tc_momento, monto_mxn,
  descripcion, notas, creado_por, creado
)
select
  e.fecha,
  'f4c3d55f-be2a-466f-98b6-d2e94d66d95c'::uuid,  -- "COMSIONES EXTRA"
  e.colab,
  e.cuenta_id,
  case when e.moneda in ('USD','CAD') then e.monto_excel / e.tc else e.monto_excel end,  -- monto nativa
  e.moneda,
  e.tc,
  e.monto_excel,  -- el Excel ya estaba en MXN para USD/CAD; para MXN/HSBC es 1×monto
  'Comisiones extras a ' || initcap(e.colab),
  '[IMPORT_2025_2026]',
  '85133c85-7c94-4e9d-a33f-5dc9a7de900b'::uuid,  -- Wal
  e.fecha + time '17:00'
from extras e;

-- ─────────────────────────────────────────────────────────────
-- 11) Arqueos cuadrados por (turno, cuenta)
-- ─────────────────────────────────────────────────────────────
insert into arqueos (
  turno_id, cuenta_id, moneda,
  venta_efectivo, comisiones_pagadas, neto_esperado, neto_reportado,
  diferencia, notas, creado_por
)
select
  t.id,
  v.cuenta_id,
  v.moneda,
  sum(v.precio - coalesce(v.descuento, 0))                                as venta_efectivo,
  sum(coalesce(v.comision_monto, 0) + coalesce(v.comision_venta_monto, 0)) as comisiones,
  sum(v.precio - coalesce(v.descuento, 0) - coalesce(v.comision_monto, 0) - coalesce(v.comision_venta_monto, 0)) as neto_esperado,
  sum(v.precio - coalesce(v.descuento, 0) - coalesce(v.comision_monto, 0) - coalesce(v.comision_venta_monto, 0)) as neto_reportado,
  0,
  '[IMPORT_2025_2026]',
  '85133c85-7c94-4e9d-a33f-5dc9a7de900b'::uuid
from turnos t
join ventas v on v.turno_id = t.id
where t.notas like '%[IMPORT_2025_2026]%'
group by t.id, v.cuenta_id, v.moneda;

-- ─────────────────────────────────────────────────────────────
-- 12) Resumen
-- ─────────────────────────────────────────────────────────────
do $$
declare
  v_turnos    int;
  v_ventas    int;
  v_pagos     int;
  v_gastos    int;
  v_arqueos   int;
  v_colabs_n  int;
begin
  select count(*) into v_turnos  from turnos  where notas like '%[IMPORT_2025_2026]%';
  select count(*) into v_ventas  from ventas v
    join turnos t on t.id = v.turno_id where t.notas like '%[IMPORT_2025_2026]%';
  select count(*) into v_pagos   from venta_pagos vp
    join ventas v on v.id = vp.venta_id
    join turnos t on t.id = v.turno_id where t.notas like '%[IMPORT_2025_2026]%';
  select count(*) into v_gastos  from gastos  where notas like '%[IMPORT_2025_2026]%';
  select count(*) into v_arqueos from arqueos where notas like '%[IMPORT_2025_2026]%';
  select count(*) into v_colabs_n from colaboradoras where notas = '[IMPORT_2025_2026]';

  raise notice 'IMPORT COMPLETADO';
  raise notice '  Colaboradoras nuevas: %', v_colabs_n;
  raise notice '  Turnos:                %', v_turnos;
  raise notice '  Ventas:                %', v_ventas;
  raise notice '  Pagos (venta_pagos):   %', v_pagos;
  raise notice '  Gastos (com. extras):  %', v_gastos;
  raise notice '  Arqueos:               %', v_arqueos;
end $$;

commit;
